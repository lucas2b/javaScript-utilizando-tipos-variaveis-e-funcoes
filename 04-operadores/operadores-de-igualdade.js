console.log('Teste 1:');
if('0' == 0){
    console.log('zero string é igual a zero number');
}


console.log('Teste 2:');
//igualdade estrita
if('0' === 0){
    console.log('zero string é igual a zero number');
}


console.log('Teste 3:');
if('0' != 0){
    console.log('zero string é diferente de zero number');
}else{
    console.log('zero string é igual a zero number');
}


console.log('Teste 4:');
//estritamente diferente
if('0' !== 0){
    console.log('zero string é estritamente diferente de zero number');
}


